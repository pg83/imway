int main() {
    auto edid = hdrEdid();
    DisplayColorCapabilities caps;

    if (!parseEdidColorCapabilities(edid.data(), edid.size(), caps) ||
        !caps.valid || !caps.pq || !caps.hlg || !caps.bt2020Rgb ||
        !caps.hasPrimaries || abs(caps.primaries.rx - 665039) > 1000 ||
        !near(caps.peakNits, 603.67) || !near(caps.maxFallNits, 351.25) ||
        !(caps.minNits > 0 && caps.minNits < .001)) {
        fprintf(stderr, "bad HDR EDID parse: valid=%d pq=%d hlg=%d 2020=%d prim=%d rx=%d peak=%.2f fall=%.2f min=%.5f\n",
                caps.valid, caps.pq, caps.hlg, caps.bt2020Rgb, caps.hasPrimaries,
                caps.primaries.rx, caps.peakNits, caps.maxFallNits, caps.minNits);
        return 1;
    }

    DisplayColorCapabilities invalid;

    if (parseEdidColorCapabilities(edid.data(), 16, invalid) || invalid.valid) {
        fputs("truncated EDID accepted\n", stderr);
        return 1;
    }

    OutputConfiguration cfg;
    cfg.hdrSdrWhiteNits = 203;
    OutputColorState detected = outputColorState(cfg, caps);

    if (!detected.hdr() || !near(detected.displayPeakNits, 603.67) ||
        !near(detected.displayMaxFallNits, 351.25) || detected.bpc != 10 ||
        detected.range != OutputRange::automatic) {
        fputs("EDID volume was not applied\n", stderr);
        return 1;
    }

    cfg.displayMinNits = .02;
    cfg.displayPeakNits = 700;
    cfg.displayMaxFallNits = 350;
    cfg.bpc = 12;
    cfg.range = OutputRange::limited;
    OutputColorState overridden = outputColorState(cfg, caps);

    if (!near(overridden.displayMinNits, .02) ||
        !near(overridden.displayPeakNits, 700) ||
        !near(overridden.displayMaxFallNits, 350) || overridden.bpc != 12 ||
        overridden.range != OutputRange::limited) {
        fputs("display overrides lost\n", stderr);
        return 1;
    }

    DisplayColorCapabilities unknown;
    OutputConfiguration fallbackCfg;
    fallbackCfg.hdrSdrWhiteNits = 160;
    OutputColorState fallback = outputColorState(fallbackCfg, unknown);

    if (!near(fallback.displayPeakNits, 1000) ||
        !near(fallback.displayMaxFallNits, 400) ||
        !near(fallback.displayMinNits, .0001)) {
        fputs("HDR fallback volume changed\n", stderr);
        return 1;
    }

    OutputConfiguration sdrCfg;
    sdrCfg.bpc = 10;
    sdrCfg.range = OutputRange::full;
    OutputColorState sdr = outputColorState(sdrCfg, unknown);

    if (sdr.hdr() || sdr.bpc != 10 || sdr.range != OutputRange::full) {
        fputs("SDR signal configuration lost\n", stderr);
        return 1;
    }

    puts("display-color: EDID, fallback, and overrides ok");
    return 0;
}
